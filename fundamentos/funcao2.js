// Armazenando função em uma variável
const imprimirSoma = function (a, b) {
    console.log(a + b)
}
console.log(2 + 3)

// Armazenando uma função Arrow em uma variável
const soma = (a, b) => {        // Arrow Function (=>)
    return a + b
}
console.log(5 + 5)

// Retorno implícito
const subtracao = (a, b) => a - b
console.log(10 - 11)
